function      replacementRates = calculateCycleRatesNeq(rate_array, c_range, T)
    for k = 1:4
        eval(['k' num2str(k) ' = rate_array(:,k);'])
    end
    for r = 1:4
        eval(['r' num2str(r) ' = rate_array(:,r+4);'])
    end
    replacementRates = NaN(size(rate_array));
    
    % calculate updates
    replacementRates(:,1) = ((-1).*c_range.*k2.*(k3+k4+r1)+c_range.*k2.*k3.*(k4+r1).*T+(k3.*k4+(k4+r1).* ...
        r4).*((-1)+r3.*T)).^(-1).*(c_range.*k2.*k3.*k4+k3.*k4.*r3+(k4+r1).*r3.* ...
        r4+c_range.^2.*k2.*r2.*(k3+r1+(-1).*k3.*r1.*T)+c_range.*r2.*(k3.*r3+r1.*r3+ ...
        r1.*r4+r3.*r4+(-1).*r1.*r3.*(k3+r4).*T));
      
    replacementRates(:,2) = ((-1).*c_range.*(k3.*k4+k1.*(k3+k4+r1)+c_range.*(k3+r1).*r2)+c_range.*k3.*(k1.*(k4+ ...
        r1)+c_range.*r1.*r2).*T).^(-1).*(k3.*k4.*r3+c_range.*k3.*r2.*r3+c_range.*r1.*r2.*r3+ ...
        c_range.*r1.*r2.*r4+k4.*r3.*r4+r1.*r3.*r4+c_range.*r2.*r3.*r4+(-1).*c_range.*r1.* ...
        r2.*r3.*(k3+r4).*T+(-1).*k1.*(k3.*k4+(k4+r1).*r4).*((-1)+r3.*T));   
    
    replacementRates(:,3) = ((-1).*k4.*(k1+r3)+(-1).*c_range.*(k2.*(k1+k4)+r2.*r3)+k1.*k4.*r3.*T+c_range.* ...
        (k1.*k2.*(k4+r1)+r1.*r2.*r3).*T+c_range.^2.*k2.*r2.*((-1)+r1.*T)).^(-1) ...
        .*(c_range.^2.*k2.*r1.*r2+(k4+r1).*r4.*(k1+r3+(-1).*k1.*r3.*T)+c_range.*(k1.* ...
        k2.*(k4+r1)+r2.*r3.*r4+r1.*r2.*(r3+r4+(-1).*r3.*r4.*T)));
      
    replacementRates(:,4) = (c_range.*k1.*k2.*(k3+r1+(-1).*k3.*r1.*T)+c_range.^2.*k2.*r2.*(k3+r1+(-1).* ...
        k3.*r1.*T)+r1.*r4.*(k1+r3+(-1).*k1.*r3.*T)+c_range.*r2.*(k3.*r3+r1.*r3+ ...
        r1.*r4+r3.*r4+(-1).*r1.*r3.*(k3+r4).*T)).*((-1).*c_range.*k2.*(k1+k3)+ ...
        c_range.*k1.*k2.*k3.*T+(k3+r4).*((-1).*r3+k1.*((-1)+r3.*T))).^(-1);
            
    replacementRates(:,5) = ((-1).*(k1+r3).*r4+(-1).*c_range.*(k1.*k2+r2.*(r3+r4))+k1.*r3.*r4.*T+c_range.* ...
        (k1.*k2.*k3+r2.*r3.*(k3+r4)).*T+c_range.^2.*k2.*r2.*((-1)+k3.*T)).^(-1) ...
        .*(c_range.^2.*k2.*k3.*r2+k4.*(k3+r4).*(k1+r3+(-1).*k1.*r3.*T)+c_range.*(k2.* ...
        k3.*k4+r2.*r3.*(k3+r4)+k1.*k2.*(k3+k4+(-1).*k3.*k4.*T)));
      
    replacementRates(:,6) = c_range.^(-1).*((-1).*(k3+r1).*(c_range.*k2+r3)+(-1).*(r1+r3).*r4+r1.*(c_range.*k2.* ...
        k3+r3.*(k3+r4)).*T).^(-1).*(c_range.*k2.*(k3.*k4+k1.*(k3+k4+r1+(-1).* ...
        k3.*(k4+r1).*T))+(-1).*(k3.*k4+(k4+r1).*r4).*((-1).*r3+k1.*((-1)+ ...
        r3.*T)));  
    
    replacementRates(:,7) = ((-1).*c_range.*r2.*(r1+r4)+c_range.*r1.*r2.*r4.*T+k3.*k4.*((-1)+k1.*T)+(k4+ ...
        r1).*r4.*((-1)+k1.*T)+c_range.*k3.*r2.*((-1)+r1.*T)).^(-1).*(k1.*k3.*k4+ ...
        k1.*(k4+r1).*r4+c_range.^2.*k2.*r2.*(k3+r1+(-1).*k3.*r1.*T)+c_range.*(k2.*k3.* ...
        k4+r1.*r2.*r4+k1.*k2.*(k3+k4+r1+(-1).*k3.*(k4+r1).*T)));
      
    replacementRates(:,8) = ((-1).*(k4+r1).*r3+(-1).*c_range.*r2.*(r1+r3)+c_range.*r1.*r2.*r3.*T+k1.*(k4+ ...
        r1).*((-1)+r3.*T)).^(-1).*(c_range.^2.*k2.*r2.*(k3+r1+(-1).*k3.*r1.*T)+ ...
        k3.*k4.*(k1+r3+(-1).*k1.*r3.*T)+c_range.*(k2.*k3.*k4+r2.*r3.*(k3+r1+(-1) ...
        .*k3.*r1.*T)+k1.*k2.*(k3+k4+r1+(-1).*k3.*(k4+r1).*T)));   