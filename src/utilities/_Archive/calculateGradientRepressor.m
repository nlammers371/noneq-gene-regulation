function dmdcInv = calculateGradientRepressor(rate_vec,c_range)    
    for k = 1:4
        eval(['k' num2str(k) ' = rate_vec(k);'])
    end
    for r = 1:4
        eval(['r' num2str(r) ' = rate_vec(r+4);'])
    end
    dmdcInv = (c_range.^2.*k3*r1*(k4 + r4) + (k2 + r2)*(k1*(k4 + r3) + r3*r4) + ...
   c_range.*(k1*k3*(k2 + k4) + r1*r2*(k4 + r3) + r1*(r2 + r3)*r4 + ...
      k2*k3*(k4 + r4))).^2 ./ ...
      (-(k1*k2*(k4 + r3) + (k2 + c_range.*r1)*r3*r4)*(k1*k3*(k2 + ...
         k4) + k2*k3*(k4 + r4) + r1*(r2*(k4 + r3) + (r2 + r3)*r4 + 2*c_range.*k3*(k4 + r4))) + ...
   r1*r3*r4*(c_range.^2*k3*r1*(k4 + r4) + (k2 + r2)*(k1*(k4 + r3) + r3*r4) + ...
      c_range.*(k1*k3*(k2 + k4) + r1*r2*(k4 + r3) + r1*(r2 + r3)*r4 + k2*k3*(k4 + r4))));